# the end number of range is not included.
for i in range(10, 16):
    print("i is now {}".format(i))
print()
for i in range(10):
    print("i is now {}".format(i))
print()
for i in range(0, 10, 2):
    print("i is now {}".format(i))
print()
for i in range(10, 0, -2):
    print("i is now {}".format(i))

